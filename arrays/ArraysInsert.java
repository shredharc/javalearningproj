package arrays;

public class ArraysInsert {
    public static void main(String[] args) {
        int [] x = new int[5];
        x[0] = 10;
        x[1] = 20;
        x[2] = 50;
        x[3] = 30;
        x[4] = 40;
        int i=0;
        while (i<=4) {
            System.out.println(x[i]);
            i++;
        }
        
    }
    
}
